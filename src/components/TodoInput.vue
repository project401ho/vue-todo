<template>
  <div class="imputBox shadow">
      <input type="text" v-model="newTodoItem">
      <span class="addContainer">
        <i class="fas fa-plus-circle addBtn" v-on:click="addTodo"></i>      
      </span>
  </div>
</template>

<script>
export default {
  data:function(){
    return {
      newTodoItem: ""
    }
  },
  methods:{
    addTodo:  function(){
      localStorage.setItem(this.newTodoItem, this.newTodoItem)
      this.clearInput();
    },
    clearInput:function(){
      this.newTodoItem = "";
    }
  }
}
</script>

